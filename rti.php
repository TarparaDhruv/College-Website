      <p align="justify" ><strong>b. Industry   Institute Interaction</strong></p>
      <p align="justify" >Institute trains the   human resource and supply technical manpower to the industries, which is very   vital input for the industries. The success of the institution depends on how   useful their products are to the industries. Success of industries also depends   in the quality and competency of the products they take from the institutes.   Since the requirements of industries are to be satisfied by the institution and   industries ultimately get the benefit of efforts taken by the institution, the   requirements of industries can be made clear so that the institutes can plan and   implement system of education and training to meet the requirements. Any   mismatch will result in loss to both; time and energy of education system will   go waste and industry will fail to face competition to survive. Thus industries   and institutes are to get together, for their own survival. It is therefore   necessary to develop a synergic relationship between both of them. Educational   institutes should take a leading role in promoting industry Institute   Interaction (III) with industries, as they will gain much from such   interaction.</p>
      <p align="justify" >Although many things   can be done under the meaning of Interaction, mostly following are the key areas   on which the mutual interaction among Industry and</p>
      <p align="justify" >Institute can be   stressed upon.</p>
      <p align="justify" >1)   Industrial training for teachers &amp; students.</p>
      <p align="justify" >2)   Field visit by teachers &amp; students.</p>
      <p align="justify" >3)   Seminars, Extension Lectures and Conferences.</p>
      <p align="justify" >4)   Assignment of Project and R &amp; D work, Testing and consultancy work.</p>
      <p align="justify" >5)   Conduct Education Program for Industry Personnel.</p>
      <p align="justify" >6)   Curriculum framing involving Industry personnel.</p>
      <p align="justify" >&nbsp;</p>
      <p align="justify" ><strong>Benefits</strong></p>
      <p align="justify" >Through Industry   Institute Interaction (III) both Institutes and Industries can be benefited a   lot. Following are the benefits that can be derived in general.</p>
      <p align="justify" >1)   Once the students are properly trained in the institute with sufficient exposure   on industrial environment there is reduced in house training cost for them once   they are recruited.</p>
      <p align="justify" >2)   Different content updating programmes of the institute   help the industry personnel adapt to rapidly changing technology.</p>
      <p align="justify" >3)   Interaction also helps the industry and its personnel to know the advance   techniques to be applied on understanding imported technology.</p>
      <p align="justify" >4)   III helps in knowing ways and means of developing indigenous technology.</p>
      <p align="justify" >5)   R&amp;D work of the industry can be improved by sponsoring R&amp;D projects to   institutes.</p>
      <p align="justify" >6)   III also helps Industry Personnel to acquire higher qualification through   continuing Education Programmed.</p>
      <p align="justify" >7)   Institute will develop infrastructure facilities through funding of industries   on sponsored R&amp;D Projects.</p>
      <p align="justify" >8)   The quality of education and training will improve through III.</p>
      <p align="justify" >9)   Curriculum can be better designed and more acceptable to industries by involving   Industry personnel in curriculum framing.</p>
      <p align="justify" >10)   Teachers and students will realize the complexities and inter disciplinary   nature of problems that are handled in industry.</p>
      <p align="justify" >11)   Students will learn important aspects of dealing with people, communication,   motivation and organization culture and factor discipline that exists in   industries.</p>
      <p align="justify" >12)   Teachers get exposed to the operation of specialized plant and equipment, which   educational institutes do not have.</p>
      <p align="justify" >13)   Teachers orient themselves with the new and emerging area of technology in   industries.</p>
      <p align="justify" >14)   Teachers also relate theory and practical, which is needed from effective   engineering education.</p>
